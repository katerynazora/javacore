public class MathFunc {
    public int multiply(int a, int b) {
        return a * b;
    }

    public int plus(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int division(int a, int b) {
        return a / b;
    }



}
